    private void jPanel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel3MouseClicked
        // TODO add your handling code here:
        toFront();
        if(!chAgree.isSelected())
            return;
        System.out.println("hey");
        name=txtName.getText().trim();
        pwd=String.valueOf(txtPwd.getPassword());
        email=txtEmail.getText().trim();
        System.out.println(name+pwd+email);
        //for email validation in order to tell user beforehand
        if(email.contains("@") && email.substring(email.indexOf(""+'@')).contains(".com"))
        {
            emailInvalid.setText("");
            evalid=true;
        }
        else
       {
           emailInvalid.setText("Invalid email..");
           evalid=false;
           return;
       }
        System.out.println("evalid="+evalid);
        
        //for name,pwd,email validation
        if(name.length()==0  || pwd.length()==0 || email.length()==0  ||!check  || !evalid || pwd.length()==0 )
        {
            lblInvalid.setText("All Fields are compulsary and should be valid");
            return;
        }
       
        signUp();
    }//GEN-LAST:event_jPanel3MouseClicked

