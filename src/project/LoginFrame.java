    private void signIn() {
        
        sql=String.format("select * from users Where lower(name)=lower('%s') and lower(password)=lower('%s') ", name,pwd);
        try
        {
            ResultSet rs=DBConnection.executeQuery(sql);
            if(rs.next())
            {
                new NewDashBoard(name).setVisible(true);
                this.dispose();
            }
            else
            {
                lblsignin.setText("Unable to sign in");
            }
        }
        catch(SQLException e)
        {
                 System.out.println("signin fn sqlexception login frame");
        }
    }

    private void signUp() {
        //check if user already exist
        sql=String.format("select * from users where lower(name)=lower('%s')", name);
        try
        {
            ResultSet rs=DBConnection.executeQuery(sql);
            if(rs.next())
            {
            lblInvalid.setText("user already exist");
            return;
            }
        }
        catch(SQLException e)
        {
            System.out.println("signup check if user already exist login frame");
        }
        //sign up user
        sql=String.format("Insert into users values('%s','%s','%s')", name,pwd,email);
        System.out.println(sql);
        try
        {
            int result=DBConnection.executeUpdate(sql);
            if(result>0)
            {
                lblInvalid.setText("");
                lblsignin.setText("");
                new NewDashBoard(name).setVisible(true);
                this.dispose();
                 
            }
        }
        catch(SQLException e)
        {
            System.out.println("Sqlexception in signup login frame");
        }
    }
